// find the square root of given number

#include<stdio.h>
#include<math.h>



int main()
{
    int numb;

    printf("enter the number : ");
    scanf("%d",&numb);

    printf("square root is : %f",sqrt(numb));
    return 0;

}

/* output

enter the number : 256
square root is : 16.000000

*/

